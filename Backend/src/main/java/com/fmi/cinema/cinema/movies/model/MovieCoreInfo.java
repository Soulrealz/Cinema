package com.fmi.cinema.cinema.movies.model;

public class MovieCoreInfo
{

	private String name;

	private String genre;

	private int duration;

	public MovieCoreInfo()
	{
	}

	public String getName()
	{
		return name;
	}

	public String getGenre()
	{
		return genre;
	}

	public int getDuration()
	{
		return duration;
	}
}
